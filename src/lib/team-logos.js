/**
 * Get logo URL for any team
 */
export function getTeamLogo(teamName, sport = 'football') {
    // Try exact match first
    let logo = null;

    switch (sport) {
        case 'football':
        case 'soccer':
            logo = TEAM_LOGOS[teamName];
            break;
        case 'basketball':
        case 'nba':
            logo = NBA_LOGOS[teamName];
            break;
        case 'nfl':
        case 'american_football':
            logo = NFL_LOGOS[teamName];
            break;
        case 'baseball':
        case 'mlb':
            logo = MLB_LOGOS[teamName];
            break;
        case 'tennis':
            const player = TENNIS_PLAYERS[teamName];
            logo = player?.photo;
            break;
    }

    // If not found, try partial match
    if (!logo) {
        const allLogos = { ...TEAM_LOGOS, ...NBA_LOGOS, ...NFL_LOGOS, ...MLB_LOGOS };
        for (const [name, url] of Object.entries(allLogos)) {
            if (name.toLowerCase().includes(teamName.toLowerCase()) ||
                teamName.toLowerCase().includes(name.toLowerCase())) {
                logo = url;
                break;
            }
        }
    }

    return logo;
}

