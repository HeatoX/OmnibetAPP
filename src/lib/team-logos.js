/**
 * Generate a placeholder logo with team initials
 */
export function generatePlaceholderLogo(teamName) {
    const initials = teamName
        .split(' ')
        .map(word => word[0])
        .join('')
        .substring(0, 2)
        .toUpperCase();

    // Return a data URI with the initials
    return `data:image/svg+xml,${encodeURIComponent(`
    <svg width="60" height="60" viewBox="0 0 60 60" xmlns="http://www.w3.org/2000/svg">
      <defs>
        <linearGradient id="grad" x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" style="stop-color:#00ff9d;stop-opacity:1" />
          <stop offset="100%" style="stop-color:#00d4ff;stop-opacity:1" />
        </linearGradient>
      </defs>
      <circle cx="30" cy="30" r="28" fill="url(#grad)" />
      <text x="30" y="38" font-family="Arial Black" font-size="20" font-weight="bold" fill="#000" text-anchor="middle">
        ${initials}
      </text>
    </svg>
  `)}`;
}

/**
 * React-compatible logo component helper
 */
export function getLogoWithFallback(teamName, sport) {
    const logo = getTeamLogo(teamName, sport);
    return logo || generatePlaceholderLogo(teamName);
}

